describe('color', () => {
  test('from string', () => {
    expect(color('rgb(51,102,153)')).toEqual(new SRGB(0.2, 0.4, 0.6));
  });
  test('from template string', () => {
    const R = 51;
    const G = 102;
    const B = 153;
    expect(color`rgb(${R},${G},${B})`).toEqual(new SRGB(0.2, 0.4, 0.6));
  });
});

